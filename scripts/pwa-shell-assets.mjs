export function sourcePathForAppShellPath(publicPath) {
  if (publicPath === '/' || publicPath === '/index.html') return 'index.html';
  if (!publicPath.startsWith('/')) {
    throw new Error(`PWA app shell path must start with "/": ${publicPath}`);
  }
  return publicPath.slice(1);
}

function unique(values) {
  return [...new Set(values)];
}

export function appShellSourcePaths() {
  return unique(appShell.map(sourcePathForAppShellPath));
}

export const pwaAssetCopies = Object.freeze([
  ['sw.js', 'sw.js'],
  ...appShell
    .map(sourcePathForAppShellPath)
    .filter((sourcePath) => sourcePath !== 'index.html')
    .map((sourcePath) => [sourcePath, sourcePath])
]);

export function computePwaCacheVersion(rootDir = process.cwd()) {
  const hash = createHash('sha256');
  hash.update('leader-field-pwa-cache-v1\0');

  for (const sourcePath of appShellSourcePaths()) {
    const filePath = join(rootDir, sourcePath);
    if (!existsSync(filePath)) {
      throw new Error(`PWA app shell source is missing: ${sourcePath}`);
    }

    hash.update(`${sourcePath}\0`);
    hash.update(readFileSync(filePath));
    hash.update('\0');
  }

  return `leader-field-${hash.digest('hex').slice(0, 12)}`;
}

export function renderServiceWorker(rootDir = process.cwd()) {
  const runtimeSource = readFileSync(join(rootDir, 'scripts', 'sw-runtime.js'), 'utf8')
    .trimStart()
    .replace(/^\/\* global APP_SHELL, CACHE_VERSION \*\/\s*/, '');

  return [
    '// Generated by scripts/generate-pwa-service-worker.mjs.',
    '// Edit scripts/pwa-shell-assets.mjs or scripts/sw-runtime.js instead.',
    `const CACHE_VERSION = ${JSON.stringify(computePwaCacheVersion(rootDir))};`,
    `const APP_SHELL = ${JSON.stringify(appShell, null, 2)};`,
    '',
    runtimeSource,
    ''
  ].join('\n');
}

export function writeServiceWorker(rootDir = process.cwd()) {
  writeFileSync(join(rootDir, 'sw.js'), renderServiceWorker(rootDir), 'utf8');
}
